s = input ('Введите предложение = ')
k = input ('Введите букву = ')

lst = s.split ()
for i in lst [:]:
    i2 = i.lower ()
    k = k.lower ()
    if k in i2:
        lst.remove (i)
        
print (' '.join (lst))

